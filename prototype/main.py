import copy


class SelfReferencingEntity:

    def __init__(self):
        self.parent = None

    def set_parent(self, parent):
        self.parent = parent


class SomeComponent:

    def __init__(self, some_int, some_list, some_circular_reference):
        self.some_int = some_int
        self.some_list = some_list
        self.some_circular_reference = some_circular_reference

    # To override the existing copy mechanism, you will need to override the __copy__ and 
    # __deepcopy__ method
    def __copy__(self):
        some_list = copy.copy(self.some_list)
        some_circular_reference = copy.copy(self.some_circular_reference)
        # self.__class__ will reference the subclass
        new = self.__class__(self.some_int, some_list, some_circular_reference)
        print('This is the current dict')
        print(self.__dict__)
        new.__dict__.update(self.__dict__)
        print('This is the new dict')
        print(new.__dict__)

    
    def __deepcopy__(self, memo=None):
        if memo is None:
            memo = {}

        some_list = copy.deepcopy(self.some_list, memo)
        some_circular_reference = copy.deepcopy(self.some_circular_reference, memo)

        new = self.__class__(self.some_int, some_list, some_circular_reference)

        new.__dict__ = copy.deepcopy(self.__dict__, memo)

        return new


if __name__ == "__main__":

    list_of_objects = [1, {1, 2, 3}, [1, 2, 3]]
    circular_ref = SelfReferencingEntity()
    component = SomeComponent(23, list_of_objects, circular_ref)
    circular_ref.set_parent(component)

    shallow_copied_component = copy.copy(component)

    # Let's change the list in shallow_copied_component and see if it changes in
